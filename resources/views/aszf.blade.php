            <p><strong>6. A szolgáltatás szüneteltetése és megszüntetése</strong><br>
            A Szolgáltató fenntartja a jogot, hogy bármikor szüneteltesse, módosítsa, vagy megszüntesse a Weboldalon elérhető szolgáltatásokat. A felhasználók a Weboldalon történő regisztrációval tudomásul veszik, hogy a Szolgáltató nem vállal felelősséget a szolgáltatások esetleges szüneteltetéséből, megszüntetéséből eredő károkért.</p>

            <p><strong>7. Adatvédelem</strong><br>
            A Weboldalon végzett tevékenységek során a Szolgáltató az adatkezelésről és adatvédelmi irányelvekről szóló tájékoztatót biztosít. A felhasználó hozzájárulását adja személyes adatainak kezeléséhez a regisztrációval és a szolgáltatások igénybevételével.</p>

