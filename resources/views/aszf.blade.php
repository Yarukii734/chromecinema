            <h2>ÁLTALÁNOS SZERZŐDÉSI FELTÉTELEK (ÁSZF)</h2>

            <p><strong>Bevezetés</strong><br>
            A ChromeCinema.hu weboldalt (a továbbiakban: "Weboldal") üzemeltető cég a [ChromeCinema] (a továbbiakban: "Szolgáltató") által biztosított szolgáltatásokat igénybe véve a felhasználók elfogadják az alábbi Általános Szerződési Feltételek (ÁSZF) rendelkezéseit. Kérjük, figyelmesen olvassa el az alábbi feltételeket, mivel a Weboldal használata ezen feltételek elfogadását jelenti.</p>

            <p><strong>1. A szolgáltatás leírása</strong><br>
            A ChromeCinema.hu weboldal a felhasználók számára filmek és moziélmények megtekintésére, vásárlására, illetve különböző filmes tartalmakhoz való hozzáférés biztosítására szolgál. A Szolgáltató minden jogot fenntart, hogy a szolgáltatásait bármikor módosítsa, frissítse, vagy szüneteltesse.</p>

            <p><strong>2. Regisztráció és fiókkezelés</strong><br>
            A Weboldalon való regisztráció önkéntes, és a felhasználók a regisztráció során pontos és valós adatokat kötelesek megadni. A felhasználó felelőssége biztosítani, hogy a megadott adatok mindig naprakészek legyenek.</p>

            <p>A felhasználó felelős az általa regisztrált fiók védelméért, valamint a fiókhoz való hozzáférés során történt minden tevékenységért.</p>

            <p><strong>3. A szolgáltatás használata</strong><br>
            A Weboldalon elérhető szolgáltatások kizárólag személyes, nem kereskedelmi célú használatra vehetők igénybe. A felhasználó nem jogosult a szolgáltatások másolására, terjesztésére, módosítására, vagy harmadik fél számára történő biztosítására.</p>

            <p><strong>4. Vásárlás és fizetés</strong><br>
            A Weboldalon elérhető szolgáltatások igénybevételéhez vásárlásra kerülhet sor, amelyet a felhasználók online, bankkártyás, vagy egyéb, a Weboldalon elérhető fizetési módokon keresztül végezhetnek el. A vásárlás során a felhasználó köteles megadni a szükséges adatokat, és elismeri, hogy a fizetés sikeres feldolgozása esetén a szolgáltatásokat igénybe vehet.</p>

