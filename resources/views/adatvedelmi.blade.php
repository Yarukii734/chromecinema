            <h2>ADATVÉDELMI TÁJÉKOZTATÓ</h2>
    
            <p><strong>Bevezetés</strong><br>
            A ChromeCinema.hu weboldal (a továbbiakban: "Weboldal") működtetője, a ChromeCinema Kft. (székhely: 4700 Mátészalka, Kölcsey utca 12., e-mail: info@chromecinema.hu, telefonszám: +36 30 8770689) kiemelten fontosnak tartja a felhasználói személyes adatainak védelmét. Az alábbiakban tájékoztatást adunk arról, hogyan kezeljük a felhasználók személyes adatait.</p>
    
            <p><strong>1. Az adatkezelés célja</strong><br>
            A Weboldal használata során gyűjtött személyes adatokat az alábbi célokból kezeljük:
            <br>- Regisztráció és fiókkezelés,
            <br>- Mozijegy-vásárlás és foglalás,
            <br>- Hűségprogramok és kedvezmények biztosítása,
            <br>- Szolgáltatások nyújtása és fejlesztése,
            <br>- Online fizetések lebonyolítása,
            <br>- Hírlevelek és promóciós anyagok küldése (hozzájárulás esetén),
            <br>- Panaszkezelés és ügyfélszolgálati kapcsolattartás,
            <br>- Filmelőzetesek és egyedi ajánlatok küldése,
            <br>- Jogi kötelezettségek teljesítése.</p>
    
            <p><strong>2. Az adatkezelés jogalapja</strong><br>
            A személyes adatok kezelésének jogalapja lehet:
            <br>- Az érintett hozzájárulása,
            <br>- Szerződés teljesítése,
            <br>- Jogi kötelezettség teljesítése,
            <br>- Jogos érdek (pl. visszaélések megelőzése, ügyfélélmény javítása).</p>
    
            <p><strong>3. A kezelt adatok köre</strong><br>
            A Weboldalon gyűjtött személyes adatok a következők lehetnek:
            <br>- Név,
            <br>- E-mail cím,
            <br>- Felhasználónév,
            <br>- Számlázási és szállítási cím,
            <br>- Fizetési információk (pl. tranzakciós adatok),
            <br>- IP-cím, böngészési adatok,
            <br>- Mozijegy-vásárlási és foglalási előzmények,
            <br>- Hűségprogramhoz kapcsolódó adatok,
            <br>- Kedvenc filmek és műfajok,
            <br>- Részvétel promóciókban és nyereményjátékokban.</p>
    
            <p><strong>4. Az adatok tárolásának időtartama</strong><br>
            A személyes adatokat addig őrizzük meg, ameddig az a fenti célok teljesítéséhez szükséges, illetve a vonatkozó jogszabályok előírásainak megfelelően.</p>
    
            <p><strong>5. Az adatok megosztása és továbbítása</strong><br>
            A Weboldal csak az alábbi esetekben osztja meg a személyes adatokat harmadik felekkel:
            <br>- Számlázási és fizetési szolgáltatókkal,
            <br>- Jogi kötelezettség teljesítése érdekében hatóságokkal,
            <br>- Weboldal üzemeltetési és marketing szolgáltatókkal,
            <br>- Filmforgalmazó partnerekkel (pl. elővételes jegyvásárlások esetén),
            <br>- Hűségprogram és promóciós kampányok lebonyolítóival.</p>
    
            <p><strong>6. Felhasználói jogok</strong><br>
            A felhasználóknak joguk van:
            <br>- Hozzáférni a róluk tárolt személyes adatokhoz,
            <br>- Kérni az adatok helyesbítését vagy törlését,
            <br>- Tiltakozni az adatkezelés ellen,
            <br>- Kérni az adatok hordozhatóságát,
            <br>- Panaszt tenni az illetékes adatvédelmi hatóságnál.</p>
    
            <p><strong>7. Adatbiztonság</strong><br>
            A ChromeCinema Kft. különös figyelmet fordít az adatbiztonságra, és megteszi a szükséges technikai és szervezeti intézkedéseket a személyes adatok védelme érdekében. Az adatokhoz való hozzáférést kizárólag jogosultsággal rendelkező munkatársak és partnerek kapják meg.</p>
    
            <p><strong>8. Cookie-k (sütik) kezelése</strong><br>
            A Weboldal cookie-kat használ a felhasználói élmény javítására, beleértve a filmelőzetesek és ajánlatok testreszabását. A felhasználók bármikor módosíthatják cookie-beállításaikat a böngészőben.</p>
    
